import redux from "@reduxjs/toolkit";

const { createSlice } = redux;
const initialState = {
  numOfCakes: 10,
};

const cakeSlice = createSlice({
  name: "cake",
  initialState,
  reducers: {
    ordered: (state) => {
      state.numOfCakes--;
    },
    restocked: (state, action) => {
      state.numOfCakes += action.payload;
    },
  },
});

// console.log(cakeSlice);
export { cakeSlice };
